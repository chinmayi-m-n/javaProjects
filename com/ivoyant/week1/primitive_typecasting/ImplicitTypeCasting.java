package com.ivoyant.week1.primitive_typecasting;

public class ImplicitTypeCasting {
    public static void main(String[] args) {
        //implicit typecasting happens when u try to store smaller datatype to larger datatype
        //trying to put cup of water to bucket
        float f;
        int i;
        i=10;
        f=i;//trying to store small datatype value to large datatype
        System.out.println(f);
    }
}
